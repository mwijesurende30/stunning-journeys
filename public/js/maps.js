// Scale all maps 1.5× (20×15 → 30×23)
(function scaleAllMaps() {
  MAPS.forEach((map) => {
    const dstCols = Math.round(map.cols * 1.5);
    const dstRows = Math.round(map.rows * 1.5);
    const scaled = [];
    for (let r = 0; r < dstRows; r++) {
      const srcR = Math.min(Math.floor(r * map.rows / dstRows), map.rows - 1);
      const row = [];
      for (let c = 0; c < dstCols; c++) {
        const srcC = Math.min(Math.floor(c * map.cols / dstCols), map.cols - 1);
        row.push(map.tiles[srcR][srcC]);
      }
      scaled.push(row);
    }
    map.tiles = scaled;
    map.cols = dstCols;
    map.rows = dstRows;
  });
})();
